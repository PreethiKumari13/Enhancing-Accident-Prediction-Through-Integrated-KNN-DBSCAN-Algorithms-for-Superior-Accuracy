import pandas as pd
import numpy as np
from sklearn.preprocessing import StandardScaler
from sklearn.cluster import DBSCAN
from sklearn.model_selection import train_test_split
from sklearn.neighbors import KNeighborsClassifier
from sklearn.ensemble import RandomForestClassifier
from sklearn.metrics import accuracy_score, classification_report, confusion_matrix
import matplotlib.pyplot as plt
import seaborn as sns

# -----------------------------
# 1) Load dataset
# -----------------------------
df = pd.read_csv("perfect_accident_dataset_final.csv")

# Features and target
X = df.drop("Accident_severity", axis=1)
y = df["Accident_severity"]

# -----------------------------
# 2) Scale features
# -----------------------------
scaler = StandardScaler()
X_scaled = scaler.fit_transform(X)

# -----------------------------
# 3) DBSCAN for cleaning outliers
# -----------------------------
dbscan = DBSCAN(eps=3, min_samples=5)
clusters = dbscan.fit_predict(X_scaled)

# Keep only points labeled as cluster (not -1)
mask = clusters != -1
X_clean = X_scaled[mask]
y_clean = y[mask]

print("Original dataset:", X.shape)
print("After DBSCAN cleaning:", X_clean.shape)

# -----------------------------
# 4) Train-test split
# -----------------------------
X_train, X_test, y_train, y_test = train_test_split(
    X_clean, y_clean, test_size=0.2, random_state=42, stratify=y_clean
)

# -----------------------------
# 5) Train KNN
# -----------------------------
knn = KNeighborsClassifier(n_neighbors=5)
knn.fit(X_train, y_train)
y_pred_knn = knn.predict(X_test)

print("\nKNN Accuracy after DBSCAN:", accuracy_score(y_test, y_pred_knn))
print("\nKNN Classification Report:\n", classification_report(y_test, y_pred_knn))

# -----------------------------
# 6) Train Random Forest
# -----------------------------
rf = RandomForestClassifier(n_estimators=100, random_state=42)
rf.fit(X_train, y_train)
y_pred_rf = rf.predict(X_test)

print("\nRandom Forest Accuracy after DBSCAN:", accuracy_score(y_test, y_pred_rf))
print("\nRandom Forest Classification Report:\n", classification_report(y_test, y_pred_rf))

# -----------------------------
# 7) Confusion Matrix Visualization
# -----------------------------
plt.figure(figsize=(6,5))
cm = confusion_matrix(y_test, y_pred_rf)
