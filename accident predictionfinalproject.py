import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler, MinMaxScaler
from sklearn.neighbors import KNeighborsClassifier
from sklearn.metrics import classification_report, confusion_matrix, accuracy_score
from sklearn.cluster import DBSCAN

import joblib   # for saving models

# Step 1: Load dataset
df = pd.read_csv("perfect_accident_dataset_final.csv")   # adjust path/filename

print("Dataset shape:", df.shape)
print("Columns:", df.columns)
print(df.head())
print(df.info())

# Step 2: Correlation heatmap to inspect relationships
plt.figure(figsize=(16,12))
corr = df.corr()
sns.heatmap(corr, annot=True, fmt=".2f", cmap="coolwarm")
plt.title("Correlation Heatmap of Features")
plt.show()

# Step 3: Preprocessing
# Example: drop or fill missing values (if any)
df = df.dropna()    # or use df.fillna(...)
# Separate features and target
X = df.drop("Accident_severity", axis=1)
y = df["Accident_severity"]

# If there are categorical variables, encode them here (e.g. pd.get_dummies)

# Scaling / normalization
scaler = StandardScaler()
X_scaled = scaler.fit_transform(X)

# Step 4: Train/Test split
X_train, X_test, y_train, y_test = train_test_split(
    X_scaled, y, test_size=0.3, random_state=42, stratify=y
)

# Step 5: Train KNN classifier
k = 5   # you can tune this later
knn = KNeighborsClassifier(n_neighbors=k)
knn.fit(X_train, y_train)

# Step 6: Prediction and evaluation
y_pred = knn.predict(X_test)
print("Accuracy:", accuracy_score(y_test, y_pred))
print("Confusion Matrix:\\n", confusion_matrix(y_test, y_pred))
print("Classification Report:\\n", classification_report(y_test, y_pred))

# Step 7 (optional): Accident hotspot clustering — only if you have location data (lat, lon) or meaningful features
# Replace 'latitude', 'longitude' with actual column names if present
if "latitude" in df.columns and "longitude" in df.columns:
    coords = df[["latitude","longitude"]].values
    db = DBSCAN(eps=0.01, min_samples=5).fit(coords)   # tune eps/min_samples
    df["cluster_label"] = db.labels_
    print("Cluster counts:", df["cluster_label"].value_counts())

